char	*ft_strrchr(const char *s, int c)
{
	char	*m;

	m = 0;
	while (*s)
	{
		if (*s == (char) c)
		{
			m = (char *) s;
		}
		s++;
	}
	if ((char) c == '\0')
	{
		return ((char *) s);
	}
	return (m);
}
